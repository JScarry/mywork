import math  #import math function
numberToFloor = float(input("Enter a number:\n"))
flooredNumber = math.floor(numberToFloor) #use math.floor function. number is now floored and equated to flooredNumber.

print (flooredNumber)
print ("The number {} floored is {}".format(numberToFloor,flooredNumber))


